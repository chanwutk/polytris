#!/usr/local/bin/python

import os
import json
import numpy as np

from polyis.io import cache
from polyis.utilities import get_config

CONFIG = get_config()
DATASETS = CONFIG['EXEC']['DATASETS']
TRACKERS = CONFIG['EXEC']['TRACKERS']
TILE_SIZES = CONFIG['EXEC']['TILE_SIZES']

def sanitize_macro_name(name: str) -> str:
    name = name.lower()
    mapping = {
        '0': 'Zero', '1': 'One', '2': 'Two', '3': 'Three', '4': 'Four',
        '5': 'Five', '6': 'Six', '7': 'Seven', '8': 'Eight', '9': 'Nine',
        '-': ' ', '_': ' '
    }
    for k, v in mapping.items():
        name = name.replace(k, v)
    return ''.join(word.capitalize() for word in name.split())

def main():
    out_dir = os.path.join('paper', 'figures', 'generated')
    os.makedirs(out_dir, exist_ok=True)
    tex_path = os.path.join(out_dir, 'p017_mistrack_variance.tex')

    all_combined = []

    with open(tex_path, 'w') as f:
        f.write('% Auto-generated by evaluation/p017_mistrack_variance.py\n')
        
        for dataset in DATASETS:
            dataset_macro = sanitize_macro_name(dataset)
            
            for tracker_name in TRACKERS:
                tracker_macro = sanitize_macro_name(tracker_name)
                
                for tile_size in TILE_SIZES:
                    tile_macro = sanitize_macro_name(str(tile_size))
                    
                    try:
                        data_dir = cache.index(dataset, 'track_rates', f'{tracker_name}_{tile_size}')
                        counts_path = data_dir / 'counts.npy'
                        rates_path = data_dir / 'sample_rates.json'
                        
                        if not counts_path.exists() or not rates_path.exists():
                            continue
                            
                        with open(rates_path, 'r') as fp:
                            sample_rates = json.load(fp)
                            
                        if 4 not in sample_rates:
                            continue
                            
                        rate_idx = sample_rates.index(4)
                        counts = np.load(counts_path)
                        
                        correct = counts[rate_idx, :, :, 0]
                        incorrect = counts[rate_idx, :, :, 1]
                        total = correct + incorrect
                        
                        valid = total > 0
                        if not np.any(valid):
                            continue
                            
                        mistrack_pct = 100.0 * incorrect[valid] / total[valid]
                        all_combined.extend(mistrack_pct.tolist())
                        
                        var = np.var(mistrack_pct)
                        std = np.std(mistrack_pct)
                        
                        macro_suffix = f'{dataset_macro}{tracker_macro}Ts{tile_macro}'
                        var_macro = f'\\newcommand{{\\mistrackVar{macro_suffix}}}{{\\autogen{{{var:.1f}}}}}\n'
                        std_macro = f'\\newcommand{{\\mistrackStd{macro_suffix}}}{{\\autogen{{{std:.1f}}}}}\n'
                        f.write(var_macro)
                        f.write(std_macro)
                        
                        print(f"Generated for {dataset} {tracker_name} {tile_size}: Var={var:.1f}, Std={std:.1f}")
                    except Exception as e:
                        print(f"Error processing {dataset} {tracker_name} {tile_size}: {e}")
                        
        if all_combined:
            mistrack_pct_combined = np.array(all_combined)
            var_combined = np.var(mistrack_pct_combined)
            std_combined = np.std(mistrack_pct_combined)
            
            f.write(f'\\newcommand{{\\mistrackVarAllDatasets}}{{\\autogen{{{var_combined:.1f}}}}}\n')
            f.write(f'\\newcommand{{\\mistrackStdAllDatasets}}{{\\autogen{{{std_combined:.1f}}}}}\n')
            
            print(f"Generated for All Datasets Combined: Var={var_combined:.1f}, Std={std_combined:.1f}")

if __name__ == '__main__':
    main()
