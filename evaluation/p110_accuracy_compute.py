class NumpyEncoder(json.JSONEncoder):
    @override
    def default(self, o):
        if isinstance(o, np.ndarray):
            return o.tolist()
        if isinstance(o, np.floating):
            return float(o)
        if isinstance(o, np.integer):
            return int(o)
        return super().default(o)


def parse_args():
    parser = argparse.ArgumentParser(description='Evaluate tracking accuracy using TrackEval')
    parser.add_argument('--metrics', type=str, default='HOTA',  # ,Identity,CLEAR',
                        help='Comma-separated list of metrics to evaluate')
    parser.add_argument('--no_parallel', action='store_true', default=False,
                        help='Whether to disable parallel processing')
    return parser.parse_args()


def resolve_groundtruth_tracking_dataset(dataset: str) -> str:
    """
    Resolve the dataset that provides tracking groundtruth for evaluation.

    Detector variants for caldot and ams should be evaluated against the
    corresponding base dataset tracking groundtruth.

    Args:
        dataset (str): Dataset name used for tracking outputs

    Returns:
        str: Dataset name used for tracking groundtruth
    """
    # Map caldot1 detector variants to the shared caldot1 groundtruth tracks.
    if dataset.startswith('caldot1-y'):
        return 'caldot1'
    # Map caldot2 detector variants to the shared caldot2 groundtruth tracks.
    if dataset.startswith('caldot2-y'):
        return 'caldot2'
    # Map ams detector variants to the shared ams groundtruth tracks.
    if dataset.startswith('ams-y'):
        return 'ams'
    # Keep all other datasets on their own groundtruth tracks.
    return dataset


def resolve_reference_tracking_source(videoset: str, track_execution_dir: str,
                                      groundtruth_execution_dir: str) -> tuple[str, str]:
    """
    Resolve the reference tracking source used as GT for evaluation.

    Validation split always uses naive tracking generated by
    preprocess/p002_preprocess_naive_tracking.py as pseudo-groundtruth.

    Args:
        videoset (str): Split name (`valid`, `test`, or `train`)
        track_execution_dir (str): Execution root for evaluated tracks
        groundtruth_execution_dir (str): Execution root for groundtruth tracks

    Returns:
        tuple[str, str]: Pair of (input_gt_dir, input_gt_relative_path)
    """
    # Use validation pseudo-groundtruth source for valid split.
    if videoset == 'valid':
        return track_execution_dir, os.path.join('002_naive', 'tracking.jsonl')

    # Use canonical groundtruth source for non-validation splits.
    assert videoset in {'test', 'train'}, f"Unknown videoset: {videoset}"
    return groundtruth_execution_dir, os.path.join('003_groundtruth', 'tracking.jsonl')


def videoset_from_video_name(video_filename: str) -> str:
    # Infer validation split from `va` prefix.
    if video_filename.startswith('va'):
        return 'valid'
    # Infer test split from `te` prefix.
    if video_filename.startswith('te'):
        return 'test'
    # Infer train split from `tr` prefix.
    if video_filename.startswith('tr'):
        return 'train'
    # Reject unknown prefixes to avoid silent split mistakes.
    raise ValueError(f"Unknown video prefix for videoset inference: {video_filename}")


def find_tracking_results(dataset: str) -> dict[str, tuple[set[str], set[str]]]:
    """
    Find all videos and tracking parameter strings with available tracking results.

    Scans the execution directory and groups discovered tracking outputs by videoset.
    Each videoset is evaluated independently in TrackEval.

    Args:
        cache_dir (str): Cache directory path
        dataset (str): Dataset name

    Returns:
        dict[str, tuple[set[str], set[str]]]: Mapping of videoset -> (videos, param_strs)
    """
    # Construct path to dataset execution directory.
    dataset_cache_dir = cache.execution(dataset)
    assert os.path.exists(dataset_cache_dir), f"Dataset cache directory {dataset_cache_dir} does not exist"

    # Resolve dataset root that supplies tracking groundtruth for this dataset.
    groundtruth_dataset = resolve_groundtruth_tracking_dataset(dataset)
    # Build execution path for resolved groundtruth dataset.
    groundtruth_execution_dir = cache.execution(groundtruth_dataset)
    assert os.path.exists(groundtruth_execution_dir), \
        f"Groundtruth execution directory {groundtruth_execution_dir} does not exist"

    # Collect discovered video/param pairs per videoset.
    split_video_param: dict[str, set[tuple[str, str]]] = {}

    # Iterate through all entries under execution root.
    for video_filename in sorted(os.listdir(dataset_cache_dir)):
        # Build absolute path to this execution item.
        video_dir = os.path.join(dataset_cache_dir, video_filename)
        # Skip non-directory entries.
        assert os.path.isdir(video_dir), f"Video directory {video_dir} is not a directory"
        assert video_dir.endswith('.mp4'), f"Video directory {video_dir} is not a video file"

        # Resolve tracking-results directory for this video.
        tracking_dir = os.path.join(video_dir, '060_uncompressed_tracks')
        # Skip videos without tracking outputs.
        assert os.path.exists(tracking_dir), f"Tracking directory {tracking_dir} does not exist"

        # Resolve videoset from filename prefix.
        videoset = videoset_from_video_name(video_filename)

        # Resolve split-level GT source path template.
        input_gt_dir, input_gt_rel_path = resolve_reference_tracking_source(
            videoset, dataset_cache_dir, groundtruth_execution_dir
        )
        # Validate GT source exists for this video.
        gt_source_path = os.path.join(input_gt_dir, video_filename, input_gt_rel_path)
        assert os.path.exists(gt_source_path), f"Reference tracking path {gt_source_path} does not exist"

        # Iterate through per-configuration tracking directories.
        for param_str in sorted(os.listdir(tracking_dir)):
            # Construct path to tracking result file.
            tracking_path = os.path.join(tracking_dir, param_str, 'tracking.jsonl')
            # Ensure tracking result file exists.
            assert os.path.exists(tracking_path), f"Tracking path {tracking_path} does not exist"
            # Initialize videoset bucket lazily.
            split_video_param.setdefault(videoset, set())
            # Add discovered video/param tuple.
            split_video_param[videoset].add((video_filename, param_str))
            # Log discovery for traceability.
            print(f"Found tracking results: videoset={videoset}, video={video_filename}, params={param_str}")

    # Build normalized output mapping with completeness validation.
    split_results: dict[str, tuple[set[str], set[str]]] = {}

    # Iterate over each videoset bucket.
    for videoset, video_param_pairs in split_video_param.items():
        # Extract unique videos for this videoset.
        videos = set(video for video, _ in video_param_pairs)
        # Extract unique parameter strings for this videoset.
        param_strs = set(param_str for _, param_str in video_param_pairs)

        # Ensure videoset has at least one video.
        assert videos, f"No videos found for videoset {videoset} in dataset {dataset}"
        # Ensure videoset has at least one parameter configuration.
        assert param_strs, f"No tracking configurations found for videoset {videoset} in dataset {dataset}"

        # Verify every video has every discovered configuration.
        for video in videos:
            for param_str in param_strs:
                assert (video, param_str) in video_param_pairs, \
                    f"Missing tracking config {param_str} for video {video} ({dataset}, {videoset})"

        # Save normalized split-level discovery output.
        split_results[videoset] = (videos, param_strs)

    # Return split-level tracking discovery output.
    return split_results


def evaluate_tracking_accuracy(dataset_root: str, dataset_name: str, videoset: str, videos: set[str],
                               param_str: str, metrics_list: list[str], output_dir: str,
                               worker_id: int, worker_id_queue: "mp.Queue"):
    """
    Evaluate tracking accuracy for one videoset and one tracking configuration.

    Args:
        dataset_root (str): Root dataset name for execution directory resolution
        dataset_name (str): Evaluation dataset name (`dataset` or `dataset-val`)
        videoset (str): Videoset name (`valid`, `test`, or `train`)
        videos (set[str]): Set of videos to evaluate
        param_str (str): Tracking parameter directory string from stage 060
        metrics_list (list[str]): List of metrics to evaluate
        output_dir (str): Output directory for results
    """
    # Parse encoded tracking parameters for metadata fields.
    parsed_params = parse_execution_param_str(param_str)
    # Resolve classifier from parsed parameter string.
    classifier = parsed_params['classifier']
    # Resolve tile size from parsed parameter string.
    tilesize = parsed_params['tilesize']
    # Resolve sample rate from parsed parameter string.
    sample_rate = parsed_params.get('sample_rate', 1)
    # Resolve tilepadding from parsed parameter string.
    tilepadding = parsed_params.get('tilepadding')
    # Resolve canvas scale from parsed parameter string.
    canvas_scale = parsed_params.get('canvas_scale')
    # Resolve tracker name from parsed parameter string.
    tracker_name = parsed_params.get('tracker')
    # Resolve optional pruning threshold from parsed parameter string.
    tracking_accuracy_threshold = parsed_params.get('tracking_accuracy_threshold')

    # Ensure required dimensions are present for stage-060 parameter strings.
    assert sample_rate is not None, f"Missing sample_rate in param string: {param_str}"
    assert tilepadding is not None, f"Missing tilepadding in param string: {param_str}"
    assert canvas_scale is not None, f"Missing canvas_scale in param string: {param_str}"
    assert tracker_name is not None, f"Missing tracker in param string: {param_str}"

    # Log evaluation task details.
    print(
        f"Evaluating videoset={videoset}, dataset={dataset_name}, "
        f"classifier={classifier}, tilesize={tilesize}, sample_rate={sample_rate}, "
        f"threshold={tracking_accuracy_threshold}, tilepadding={tilepadding}, "
        f"canvas_scale={canvas_scale}, tracker={tracker_name}, videos={len(videos)}"
    )

    # Resolve execution directory that provides tracking predictions.
    track_execution_dir = cache.execution(dataset_root)
    # Resolve dataset root that provides groundtruth tracking.
    groundtruth_dataset = resolve_groundtruth_tracking_dataset(dataset_root)
    # Build execution directory path for resolved GT dataset.
    gt_execution_dir = cache.execution(groundtruth_dataset)

    # Validate tracking execution directory exists.
    assert os.path.exists(track_execution_dir), f"Tracking execution directory {track_execution_dir} does not exist"
    # Validate GT execution directory exists.
    assert os.path.exists(gt_execution_dir), f"Groundtruth execution directory {gt_execution_dir} does not exist"

    # Resolve split-level GT source path template.
    input_gt_dir, input_gt = resolve_reference_tracking_source(videoset, track_execution_dir, gt_execution_dir)

    # Validate GT source exists for every sequence in this task.
    for video in videos:
        gt_source_path = os.path.join(input_gt_dir, video, input_gt)
        assert os.path.exists(gt_source_path), f"Reference tracking path {gt_source_path} does not exist"

    # Build default tracking input path relative to execution/video directory.
    input_track = os.path.join('060_uncompressed_tracks', param_str, 'tracking.jsonl')

    # Switch to naive tracking input for explicit groundtruth baseline config.
    if classifier == 'Groundtruth' or tilesize == 0 or tilepadding == 'Groundtruth':
        # Validate canonical baseline configuration shape.
        assert classifier == 'Groundtruth' and tilesize == 0 and tilepadding == 'Groundtruth', \
            "classifier and tilepadding must be Groundtruth and tilesize must be 0 if baseline config is used, " \
            f"but got classifier={classifier}, tilesize={tilesize}, tilepadding={tilepadding}"
        # Use naive tracking as prediction input for baseline evaluation.
        input_track = os.path.join('002_naive', 'tracking.jsonl')

    # Validate tracking input exists for every sequence in this task.
    for video in videos:
        tracking_source_path = os.path.join(track_execution_dir, video, input_track)
        assert os.path.exists(tracking_source_path), f"Tracking source path {tracking_source_path} does not exist"

    # Create TrackEval dataset configuration.
    dataset_config = {
        'output_fol': output_dir,  # Where TrackEval writes outputs.
        'output_sub_fol': f'{dataset_name}_{param_str}',  # Subfolder name for this run.
        'input_gt': input_gt,  # Relative path to split-level reference tracking files.
        'input_track': input_track,  # Relative path to tracking prediction files.
        'skip': 1,  # Process every frame.
        'tracker': param_str,  # Tracker identifier shown by TrackEval.
        'seq_list': sorted(videos),  # Deterministic ordered sequence list.
        'input_dir': track_execution_dir,  # Backward-compatible base directory.
        'input_gt_dir': input_gt_dir,  # Base directory for reference tracking files.
        'input_track_dir': track_execution_dir,  # Base directory for tracking prediction files.
    }

    # Create TrackEval evaluator configuration.
    eval_config = {
        'USE_PARALLEL': False,  # Disable TrackEval internal parallelism.
        'NUM_PARALLEL_CORES': min(mp.cpu_count(), len(videos)),  # Limit cores to sequence count.
        'BREAK_ON_ERROR': True,  # Stop evaluation on first error.
        'LOG_ON_ERROR': os.path.join(output_dir, 'LOG.txt'),  # Save evaluation errors.
        'PRINT_RESULTS': False,  # Disable result printing.
        'PRINT_CONFIG': False,  # Disable config printing.
        'TIME_PROGRESS': False,  # Disable timing progress output.
        'OUTPUT_SUMMARY': False,  # Disable summary files.
        'OUTPUT_DETAILED': False,  # Disable detailed files.
        'PLOT_CURVES': False,  # Disable curve plots.
        'OUTPUT_EMPTY_CLASSES': False,  # Disable empty-class output rows.
    }

    # Ensure output directory exists before evaluation starts.
    os.makedirs(output_dir, exist_ok=True)

    # Create TrackEval metric objects based on requested metrics.
    metrics = []
    # Iterate over requested metric names.
    for metric_name in metrics_list:
        # Add HOTA metric evaluator.
        if metric_name == 'HOTA':
            metrics.append(HOTA())
        # Add CLEAR metric evaluator.
        elif metric_name == 'CLEAR':
            metrics.append(CLEAR({'THRESHOLD': 0.5, 'PRINT_CONFIG': False}))
        # Add Identity metric evaluator.
        elif metric_name == 'Identity':
            metrics.append(Identity({'THRESHOLD': 0.5}))

    # Create TrackEval dataset object.
    eval_dataset = Dataset(dataset_config)
    # Create TrackEval evaluator object.
    evaluator = trackeval.Evaluator(eval_config)

    # Suppress warnings emitted by TrackEval internals.
    with warnings.catch_warnings():
        # Ignore warning messages during evaluation.
        warnings.filterwarnings("ignore")
        # Run evaluation for this videoset/configuration task.
        results = evaluator.evaluate([eval_dataset], metrics)

    # Validate TrackEval return structure.
    assert results and len(results) == 2, results
    # Validate TrackEval status for this dataset.
    assert results[1]['Dataset']['sort'] == 'Success', f"Evaluation failed: {results[1]}"

    # Extract dataset-level result container.
    dataset_result = results[0].get('Dataset', {})
    # Extract tracker-level result container.
    tracker_results = dataset_result.get('sort', {})

    # Ensure output directory exists for result writing.
    os.makedirs(output_dir, exist_ok=True)

    # Build expected sequence tuple including combined result.
    expected_sequences = tuple(sorted(videos | {'COMBINED_SEQ'}))
    # Build actual sequence tuple from result keys.
    actual_sequences = tuple(sorted(tracker_results.keys()))
    # Validate sequence coverage.
    assert expected_sequences == actual_sequences, \
        f"Expected sequences {expected_sequences} do not match actual {actual_sequences}"

    # Iterate through sequence results (including combined sequence).
    for seq, tracker_result in tracker_results.items():
        # Validate vehicle class result exists.
        assert 'vehicle' in tracker_result, f"Vehicle results not found for {seq}"
        # Validate sequence identity.
        assert seq in videos or seq == 'COMBINED_SEQ', f"Sequence {seq} not found in {videos}"

        # Initialize metric payload dictionary.
        seq_metrics = {}
        # Resolve vehicle-class metric payload.
        vehicle_results = tracker_result['vehicle']

        # Extract requested metrics plus Count metric.
        for metric in metrics + [Count()]:
            # Resolve canonical metric name.
            metric_name = metric.get_name()
            # Validate metric exists in output payload.
            assert metric_name in vehicle_results, \
                f"Metric {metric_name} not found in {vehicle_results}"
            # Store metric payload for this metric.
            seq_metrics[metric_name] = vehicle_results[metric_name]

        # Build output record for this sequence.
        result_data = {
            'video': None if seq == 'COMBINED_SEQ' else seq,  # None for combined split-level result.
            'dataset': dataset_name,  # Split-specific dataset name.
            'videoset': videoset,  # Split label for downstream analysis.
            'classifier': classifier,  # Classifier name.
            'tilesize': tilesize,  # Tile size value.
            'tilepadding': tilepadding,  # Tilepadding mode.
            'sample_rate': sample_rate,  # Frame sample-rate.
            'tracking_accuracy_threshold': tracking_accuracy_threshold,  # Optional pruning threshold.
            'canvas_scale': canvas_scale,  # Canvas scale value.
            'tracker': tracker_name,  # Tracker name.
            'metrics': seq_metrics,  # Metric payloads.
        }

        # Resolve output filename for this sequence.
        output_file = 'DATASET' if seq == 'COMBINED_SEQ' else seq
        # Write result JSON file.
        with open(os.path.join(output_dir, f'{output_file}.json'), 'w') as f:
            json.dump(result_data, f, indent=2, cls=NumpyEncoder)

    # Return worker id to queue once task finishes.
    worker_id_queue.put(worker_id)


def main(args):
    """
    Main function that orchestrates the tracking accuracy evaluation process.

    This function serves as the entry point for the script. It:
    1. Finds all videos with tracking results for each dataset and split
    2. Groups videos by split and tracking parameter string
    3. Runs TrackEval separately per split/configuration pair
    4. Stores split-level outputs under split-specific dataset names

    Args:
        args (argparse.Namespace): Parsed command line arguments

    Note:
        - Tracking outputs are expected in:
          {CACHE_DIR}/{dataset}/execution/{video_file}/060_uncompressed_tracks/{param_str}/tracking.jsonl
        - Reference tracking uses:
          {CACHE_DIR}/{gt_dataset}/execution/{video_file}/003_groundtruth/tracking.jsonl for test/train
          {CACHE_DIR}/{dataset}/execution/{video_file}/002_naive/tracking.jsonl for valid
        - Valid split is evaluated as dataset name: {dataset}-val
        - Results are saved to:
          {CACHE_DIR}/{dataset}/evaluation/070_accuracy/raw/{dataset_or_dataset-val}/{param_str}/
    """
    print(f"Starting tracking accuracy evaluation for datasets: {DATASETS}")

    # Parse metrics from comma-separated string into list.
    metrics_list = [m.strip() for m in args.metrics.split(',')]
    # Log resolved metrics.
    print(f"Evaluating metrics: {metrics_list}")

    # Initialize task list for all split/config evaluations.
    eval_tasks: list[Callable[[int, "mp.Queue"], None]] = []

    # Process each configured dataset root.
    for dataset in DATASETS:
        # Log current dataset root.
        print(f"Processing dataset: {dataset}")

        # Discover split-level videos and parameter strings for this dataset.
        split_results = find_tracking_results(dataset)

        # Build evaluation directory path for this dataset root.
        evaluation_dir = cache.eval(dataset, 'acc')

        # Clear previous evaluation directory to avoid stale outputs.
        if os.path.exists(evaluation_dir):
            shutil.rmtree(evaluation_dir)
            print(f"Cleared existing 070_accuracy directory: {evaluation_dir}")
        # Create fresh evaluation directory.
        os.makedirs(evaluation_dir, exist_ok=True)
        # Create raw-results root directory.
        os.makedirs(os.path.join(evaluation_dir, 'raw'), exist_ok=True)

        # Iterate over discovered split buckets.
        for videoset in sorted(split_results.keys()):
            # Resolve split-specific videos and parameter strings.
            videos, param_strs = split_results[videoset]
            # Resolve split-aware dataset name (`dataset` or `dataset-val`).
            split_dataset_name = dataset_name_for_videoset(dataset, videoset)

            # Create one evaluation task per discovered stage-060 parameter string.
            for param_str in sorted(param_strs):
                # Build split-aware output directory for this task.
                output_dir = os.path.join(evaluation_dir, 'raw', split_dataset_name, param_str)
                # Create partial function with static task arguments.
                eval_tasks.append(partial(
                    evaluate_tracking_accuracy,
                    dataset,
                    split_dataset_name,
                    videoset,
                    videos,
                    param_str,
                    metrics_list,
                    output_dir,
                ))

            # Add explicit naive-baseline evaluation for this split.
            baseline_param_str = build_param_str(
                classifier='Groundtruth',
                tilesize=0,
                sample_rate=1,
                tilepadding='Groundtruth',
                canvas_scale=1.0,
                tracker='groundtruth',
            )

            # Avoid duplicating baseline task if it already exists.
            if baseline_param_str not in param_strs:
                # Build output path for baseline result.
                output_dir = os.path.join(evaluation_dir, 'raw', split_dataset_name, baseline_param_str)
                # Append baseline evaluation task.
                eval_tasks.append(partial(
                    evaluate_tracking_accuracy,
                    dataset,
                    split_dataset_name,
                    videoset,
                    videos,
                    baseline_param_str,
                    metrics_list,
                    output_dir,
                ))

    # Ensure at least one evaluation task was discovered.
    assert len(eval_tasks) > 0, "No tracking results found. Please ensure 060_exec_track.py has been run first."
    # Log task count.
    print(f"Found {len(eval_tasks)} split/configuration evaluation tasks")

    # Create worker-id queue for child-process progress routing.
    worker_id_queue = mp.Queue()
    # Seed worker-id queue with available logical worker ids.
    for i in range(max(1, int(mp.cpu_count() * 0.8))):
        worker_id_queue.put(i)

    # Run tasks either sequentially or in parallel.
    if args.no_parallel:
        # Execute tasks sequentially when parallel execution is disabled.
        for eval_task in track(eval_tasks):
            # Acquire worker id for this task.
            worker_id = worker_id_queue.get()
            # Run task in current process.
            eval_task(worker_id, worker_id_queue)
        return

    # Track spawned processes for parallel execution.
    processes: list[mp.Process] = []

    # Spawn one process per task.
    for eval_task in eval_tasks:
        # Acquire worker id for this process.
        worker_id = worker_id_queue.get()
        # Create process object for the task.
        process = mp.Process(target=eval_task, args=(worker_id, worker_id_queue))
        # Start process execution.
        process.start()
        # Append process to tracking list.
        processes.append(process)

    # Wait for all processes and terminate cleanly.
    for process in track(processes):
        # Wait for process completion.
        process.join()
        # Ensure process resources are released.
        process.terminate()


if __name__ == '__main__':
    main(parse_args())
